fav_color = "purple"
case fav_color
    when "red" then puts "Red like fire"
    when "orange" then puts "Orange like, well... an orange."
    when "yellow" then puts "Yellow daffodils are so pretty in the spring!"
    when "green" then puts "Have you been to the Emerald City in Oz?"
    when "blue" then puts "Blue like the sky!"
    when "purple" then puts "Purple plums are the tastiest."
else
    puts "Hmm, well I don't know what that color is!"
end

weather = "sunny"
case weather
    when "sunny" then puts "Sunny days are the best days of the year. Wear sunscreen and enjoy!"
    when "raining" then puts "Rain is a necessary evil, wear boots and bring an umbrella!"
    when "snowing" then puts "Brrr! Better bring a hat, gloves, scarf and boots!"
    when "foggy" then puts "Its tought to see outside, maybe wear your glasses, hehe!"
    when "windy" then puts "A light jacket and a well fitted heat will do you fine today!"
end